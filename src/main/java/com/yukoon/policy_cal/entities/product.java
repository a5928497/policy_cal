package com.yukoon.policy_cal.entities;

public class product {
    private int spec;
    private float privr;
    private int min;
    private int max;

    public int getSpec() {
        return spec;
    }

    public void setSpec(int spec) {
        this.spec = spec;
    }

    public float getPrivr() {
        return privr;
    }

    public void setPrivr(float privr) {
        this.privr = privr;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public int getMax() {
        return max;
    }

    public void setMax(int max) {
        this.max = max;
    }
}
